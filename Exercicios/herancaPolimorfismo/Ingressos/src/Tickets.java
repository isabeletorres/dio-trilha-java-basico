public class Tickets {

    private double value;
    private String name;
    private boolean dub;

    public double getValue(){
        return value;
    }
    public void setValue(double value){
        this.value = value;
    }
    public String getName(){
        return name;
    }
    public void setName(String name){
        this.name = name;
    }

    public boolean getDub(){
        return dub;
    }
    public void setDub(boolean dub){
        this.dub = dub;
    }

}
